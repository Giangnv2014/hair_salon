    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $abouts = About::all();

        foreach ($abouts as $about) {
            switch ($about->type) {
                case config('about.type.destiny'):
                    $this->data['destiny'] = $about;
                    break;
                case config('about.type.about'):
                    $this->data['abouts'][] = $about;
                    break;
                case config('about.type.passion'):
                    $this->data['passions'][] = $about;
                    break;
                default:
                    $this->data['stylists'][] = $about;
                    break;
            }
        }

        return view('frontends.abouts.index', $this->data);
    }

